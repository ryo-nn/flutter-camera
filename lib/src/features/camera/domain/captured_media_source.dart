/// 撮影結果([CapturedPhoto]/[CapturedVideo])がどこから得られたかを表す。
///
/// フォトライブラリ取り込み機能の追加により、S-04からS-05/S-05vへ渡すメディアは
/// カメラ撮影(自撮り)由来とフォトライブラリ選択由来の2系統になった。
/// カメラ由来(フロントカメラ)のみに適用され得るミラー処理・レンズ向き前提の
/// 表示分岐を、ライブラリ由来のメディアに誤って適用しないための区別に用いる。
enum CapturedMediaSource {
  /// カメラ撮影(S-04シャッター/録画ボタン)由来。
  camera,

  /// フォトライブラリ選択(image_picker `pickMedia`)由来。
  library,
}
